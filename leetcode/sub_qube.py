def sub_cube(matrix):
    i = 1

    res = []
    while i <= 3:
        j = 1
        while j <= 3:
            p_lower = (i - 1) * 3
            p = i * 3
            temp = []
            for m in range(p_lower, p):
                q_lower = (j - 1) * 3
                q = j * 3
                for n in range(q_lower, q):
                    if matrix[m][n] != '.':
                        temp.append(matrix[m][n])
            res.append(temp)
            j += 1
        i += 1

    print(res)


